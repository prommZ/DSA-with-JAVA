public class DataType {
    public static void main(String[] args) {
        int a = 10 ;
        System.out.println("integer DataType  :" + a);
        long b = 100000;
        System.out.println("long DataType  :" + b);
        float c = 1.5f;
        System.out.println( "float DataType  :" +c);
        double d = 9999.9;
        System.out.println("Double DataType  :" +d);
        byte e = 26;
        System.out.println("Byte DataType  :" +e);
        boolean f = true;
        System.out.println("boolean DataType  :" +f);
        char ch = 'x';
        System.out.println("The character is : " +ch);
        short g= 100;
        System.out.println("Short DataType  :" +g);
    }
}
